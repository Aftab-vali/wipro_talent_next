package day_1;

public class ArithmeticOperatorsDemo {
    public static void main(String[] args) {
        int a = 10, b = 4;

        System.out.println("a + b = " + (a + b));

        System.out.println("a - b = " + (a - b));

        System.out.println("a * b = " + (a * b));

        System.out.println("a / b = " + (a / b));

        System.out.println("a % b = " + (a % b));

        double c = 10, d = 4;
        System.out.println("c / d = " + (c / d));
    }
}
